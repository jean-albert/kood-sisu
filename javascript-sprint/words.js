// Function to split a string by a specified delimiter
function splitByDelimiter(inputString, delimiter) {
    return inputString.split(delimiter); // Splits the string and returns an array
}

// Function to join an array of strings with a specified delimiter
function joinWithDelimiter(arrayOfStrings, joinString) {
    return arrayOfStrings.join(joinString); // Joins the array into a single string
}

